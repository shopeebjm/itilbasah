#!/data/data/com.termux/files/usr/bin/env bash
f=100
p=2
turbo=15

M=32768 s=13 r=2000 t=0
sets=("┃┏ ┓┛━┓  ┗┃┛┗ ┏━" "│╭ ╮╯─╮  ╰│╯╰ ╭─" "│┌ ┐┘─┐  └│┘└ ┌─" "║╔ ╗╝═╗  ╚║╝╚ ╔═" "|+ ++-+  +|++ +-" "|/ \/-\  \|/\ /-" ".. ....  .... .." ".o oo.o  o.oo o." "-\ /\|/  /-\/ \|" "╿┍ ┑┚╼┒  ┕╽┙┖ ┎╾")
SETS=(); x=(); y=(); l=(); n=(); v=(); c=(); V=(); C=(); VN=0; CN=0; E=()
RNDSTART=1 BOLD=1 NOCOLOR=0 KEEPCT=0

START_TIME=$(date +%s)

cleanup() {
    tput reset; tput rmcup; tput cnorm; stty echo
    exit 0
}

resize() { w=$(tput cols); h=$(tput lines); }

init_pipes() {
    local i; ci=$((KEEPCT ? 0 : CN * RANDOM / M)); vi=$((KEEPCT ? 0 : VN * RANDOM / M))
    for ((i = 0; i < p; i++)); do
        ((n[i]=0, l[i]=RANDOM%4, x[i]=w*RANDOM/M, y[i]=h*RANDOM/M, v[i]=V[vi]))
        c[i]=${E[ci]}; ((ci=(ci+1)%CN, vi=(vi+1)%VN))
    done
}

main() {
    tput -T "$TERM" sgr0 >/dev/null || return $?
    COLORS=$(tput colors); SGR0=$(tput sgr0); SGR_BOLD=$(tput bold)
    V=(0); VN=1; C=(1 2 3 4 5 6 7 0); CN=8
    for ((i=0; i<CN; i++)); do
        E[i]=$SGR0; ((BOLD)) && E[i]+=$SGR_BOLD; ((NOCOLOR)) || E[i]+=$(tput setaf ${C[i]})
    done
    for ((i=0; i<${#sets[@]}; i++)); do
        for ((j=0; j<16; j++)); do SETS+=("${sets[i]:j:1}"); done
    done
    
    stty -echo; tput smcup; tput civis; tput clear
    trap cleanup HUP TERM; resize; trap resize SIGWINCH
    init_pipes

    while true; do
        (( $(date +%s) - START_TIME >= 5 )) && break
        
        read -t 0.01 -n 1 2>/dev/null
        [[ -n "$REPLY" ]] && break
        
        for ((step=0; step < turbo; step++)); do
            for ((i=0; i<p; i++)); do
           
                ((l[i] % 2)) && ((x[i] += -l[i] + 2, 1)) || ((y[i] += l[i] - 1))
                ((!KEEPCT && (x[i] >= w || x[i] < 0 || y[i] >= h || y[i] < 0))) && { c[i]=${E[CN * RANDOM / M]}; ((v[i] = V[VN * RANDOM / M])); }
                ((x[i] = (x[i] + w) % w, y[i] = (y[i] + h) % h))
                ((n[i] = s * RANDOM / M - 1, n[i] = n[i] >= 0 ? l[i] : l[i] + (2 * (RANDOM % 2) - 1), n[i] = (n[i] + 4) % 4))
                printf '\e[%d;%dH%s%s' $((y[i] + 1)) $((x[i] + 1)) ${c[i]} "${SETS[v[i] * 16 + l[i] * 4 + n[i]]}"
                l[i]=${n[i]}
            done
        done 
        ((r > 0 && t * p >= r)) && tput reset && tput civis && t=0 || ((t++))
    done
    cleanup
}
main "$@"
EOF
