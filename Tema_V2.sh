BACKUP_FLAG_FILE="$HOME/.termux_backup_flag"
LATEST_BACKUP_DIR=""
CURRENT_SHELL=$(basename "$SHELL")
INSTALL_LOG="$HOME/.termux_install.log"

log_message() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" >> "$INSTALL_LOG"
}

check_existing_backup() {
    if [ -f "$BACKUP_FLAG_FILE" ]; then
        LATEST_BACKUP_DIR=$(cat "$BACKUP_FLAG_FILE" 2>/dev/null)
        if [ -d "$LATEST_BACKUP_DIR" ]; then
            return 0
        else
            rm -f "$BACKUP_FLAG_FILE"
        fi
    fi
    
    local latest_backup=$(ls -d $HOME/.termux_backup_* 2>/dev/null | sort -r | head -1)
    if [ -n "$latest_backup" ] && [ -d "$latest_backup" ]; then
        LATEST_BACKUP_DIR="$latest_backup"
        echo "$latest_backup" > "$BACKUP_FLAG_FILE"
        return 0
    fi
    
    return 1
}


backup_shell_configs() {
    local backup_dir="$HOME/.termux_backup_$(date +%Y%m%d_%H%M%S)"
    
    dialog --backtitle "TERMUX THEME INSTALLER" \
           --title "🔐 MEMBUAT BACKUP" \
           --infobox "Membackup konfigurasi shell..." 5 50
    
    mkdir -p "$backup_dir"
    
    local files_to_backup=(
        "$PREFIX/etc/bash.bashrc"
        "$PREFIX/etc/zshrc"
        "$HOME/.bashrc"
        "$HOME/.zshrc"
    )
    
    local backup_names=(
        "bash.bashrc"
        "zshrc_system"
        "bashrc_user"
        "zshrc_user"
    )
    
    local backed_up=0
    local total_files=${#files_to_backup[@]}
    
    for i in "${!files_to_backup[@]}"; do
        if [ -f "${files_to_backup[$i]}" ]; then
            cp "${files_to_backup[$i]}" "$backup_dir/${backup_names[$i]}"
            backed_up=$((backed_up + 1))
        fi
    done
    
    cat > "$backup_dir/restore.sh" << 'EOF'
#!/data/data/com.termux/files/usr/bin/bash
dialog --backtitle "TERMUX BACKUP RESTORE" --title "RESTORING" --infobox "Memulai proses restore..." 5 50
sleep 2

# Restore files
[ -f "bash.bashrc" ] && cp "bash.bashrc" "$PREFIX/etc/bash.bashrc"
[ -f "zshrc_system" ] && cp "zshrc_system" "$PREFIX/etc/zshrc"
[ -f "bashrc_user" ] && cp "bashrc_user" "$HOME/.bashrc"
[ -f "zshrc_user" ] && cp "zshrc_user" "$HOME/.zshrc"

dialog --backtitle "TERMUX BACKUP RESTORE" --title "SUKSES" --msgbox "Restore berhasil!\nRestart Termux untuk efek perubahan." 7 50
EOF
    
    chmod +x "$backup_dir/restore.sh"
    cat > "$backup_dir/INFO.txt" << EOF
BACKUP TIME: $(date)
SHELL: $CURRENT_SHELL
FILES BACKED UP: $backed_up/$total_files
LOCATION: $backup_dir
EOF
    
    LATEST_BACKUP_DIR="$backup_dir"
    echo "$backup_dir" > "$BACKUP_FLAG_FILE"
    
    log_message "Backup created: $backup_dir ($backed_up files)"
    
    dialog --backtitle "TERMUX THEME INSTALLER" \
           --title "✅ BACKUP SELESAI" \
           --msgbox "Backup berhasil dibuat!\n\nLokasi: $(basename $backup_dir)\nFile: $backed_up/$total_files" 8 50
    
    return 0
}

auto_backup_configs() {
    if check_existing_backup; then
        local backup_age=$(stat -c %Y "$LATEST_BACKUP_DIR" 2>/dev/null)
        local current_age=$(date +%s)
        local diff_hours=$(( (current_age - backup_age) / 3600 ))
        
        dialog --backtitle "TERMUX THEME INSTALLER" \
               --title "🔍 DETEKSI BACKUP" \
               --yesno "Backup ditemukan:\n\n$(basename $LATEST_BACKUP_DIR)\n($diff_hours jam yang lalu)\n\nGunakan backup ini?" 10 50
        
        if [ $? -eq 0 ]; then
            return 0
        fi
    fi
    
    backup_shell_configs
    return $?
}

