extract() {
    if [[ ! -f "$1" ]]; then
        echo "❌ '$1' bukan file valid"
        return 1
    fi

    case "$1" in
        *.tar.bz2) tar xjf "$1" ;;
        *.tar.gz)  tar xzf "$1" ;;
        *.tar.xz)  tar xf  "$1" ;;
        *.zip)     unzip  "$1" ;;
        *.7z)      7z x   "$1" ;;
        *) echo "❌ Format tidak dikenali" ;;
    esac
}


