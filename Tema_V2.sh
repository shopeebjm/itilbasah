select_backup_to_restore() {
    local backups=($(ls -d "$HOME"/.termux_backup_* 2>/dev/null | sort -r))

    if [ ${#backups[@]} -eq 0 ]; then
        dialog --backtitle "TERMUX THEME INSTALLER" \
               --title "❌ TIDAK ADA" \
               --msgbox "Tidak ada backup!" 6 50
        return
    fi

    local backup_list=()
    for i in "${!backups[@]}"; do
        local name=$(basename "${backups[$i]}")
        local date=$(stat -c %y "${backups[$i]}" 2>/dev/null | cut -d' ' -f1)
        backup_list+=("$((i+1))" "$name ($date)")
    done

    local selected=$(dialog --backtitle "TERMUX THEME INSTALLER" \
                            --title "📋 PILIH BACKUP" \
                            --menu "Pilih backup untuk restore:" 15 50 5 \
                            "${backup_list[@]}" \
                            3>&1 1>&2 2>&3)

    [ -z "$selected" ] && return

    local backup_index=$((selected - 1))
    local selected_backup="${backups[$backup_index]}"

    dialog --backtitle "TERMUX THEME INSTALLER" \
           --title "🔄 RESTORE" \
           --yesno "Restore dari:\n\n$(basename "$selected_backup")\n\nKonfigurasi saat ini akan diganti!" 10 50 || return

    export NCURSES_NO_UTF8_ACS=1

    (
        echo "5";   echo "# Menyiapkan restore..."
        sleep 0.2

        if [ -f "$selected_backup/bash.bashrc" ]; then
            echo "25"; echo "# Restore bash.bashrc"
            cp "$selected_backup/bash.bashrc" "$PREFIX/etc/bash.bashrc"
            sleep 0.2
        fi

        if [ -f "$selected_backup/zshrc_system" ]; then
            echo "40"; echo "# Restore zshrc system"
            cp "$selected_backup/zshrc_system" "$PREFIX/etc/zshrc"
            sleep 0.2
        fi

        if [ -f "$selected_backup/bashrc_user" ]; then
            echo "60"; echo "# Restore .bashrc user"
            cp "$selected_backup/bashrc_user" "$HOME/.bashrc"
            sleep 0.2
        fi

        if [ -f "$selected_backup/zshrc_user" ]; then
            echo "80"; echo "# Restore .zshrc user"
            cp "$selected_backup/zshrc_user" "$HOME/.zshrc"
            sleep 0.2
        fi

        echo "90"; echo "# Set default shell ke bash"
        chsh -s bash
        sleep 0.2

        echo "100"; echo "# Restore selesai!"
    ) | dialog --backtitle "TERMUX THEME INSTALLER" \
               --title "PROGRESS" \
               --gauge "Memulihkan backup..." 8 50

    dialog --backtitle "TERMUX THEME INSTALLER" \
           --title "✅ SUKSES" \
           --msgbox "Restore berhasil!" 6 50

    log_message "Restored from: $(basename "$selected_backup")"
}

manage_backup_menu() {
    local backups=($(ls -d $HOME/.termux_backup_* 2>/dev/null | sort -r))
    
    if [ ${#backups[@]} -eq 0 ]; then
        dialog --backtitle "TERMUX THEME INSTALLER" \
               --title "📭 KOSONG" \
               --msgbox "Tidak ada backup!" 6 50
        return
    fi
    
    local backup_items=()
    for i in "${!backups[@]}"; do
        local name=$(basename "${backups[$i]}")
        local date=$(stat -c %y "${backups[$i]}" 2>/dev/null | cut -d' ' -f1)
        local file_count=$(ls -1 "${backups[$i]}" | wc -l)
        backup_items+=("$((i+1))" "$name" "$date" "$file_count files")
    done
    
    dialog --backtitle "TERMUX THEME INSTALLER" \
           --title "📋 DAFTAR BACKUP" \
           --extra-button --extra-label "OPSI" \
           --ok-label "INFO" \
           --cancel-label "KEMBALI" \
           --menu "" 15 60 5 \
           "${backup_items[@]}" \
           2> $PREFIX/tmp//backup_selection
    
    local button=$?
    local selection=$(cat $PREFIX/tmp//backup_selection 2>/dev/null)
    
    if [ $button -eq 0 ]; then  # INFO button
        if [ -n "$selection" ]; then
            local index=$((selection - 1))
            show_backup_info "${backups[$index]}"
        fi
    elif [ $button -eq 3 ]; then  # OPSI button
        show_backup_options "$selection"
    fi
}

