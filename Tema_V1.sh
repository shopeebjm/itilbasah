mkcd() {
    mkdir -p "\$1" && cd "\$1"
}

extract() {
    if [ -f "\$1" ]; then
        case "\$1" in
            *.tar.bz2)   tar xjf "\$1" ;;
            *.tar.gz)    tar xzf "\$1" ;;
            *.tar.xz)    tar xf "\$1" ;;
            *.bz2)       bunzip2 "\$1" ;;
            *.rar)       unrar x "\$1" ;;
            *.gz)        gunzip "\$1" ;;
            *.tar)       tar xf "\$1" ;;
            *.tbz2)      tar xjf "\$1" ;;
            *.tgz)       tar xzf "\$1" ;;
            *.zip)       unzip "\$1" ;;
            *.Z)         uncompress "\$1" ;;
            *.7z)        7z x "\$1" ;;
            *)           echo "'\$1' format tidak dikenali" ;;
        esac
    else
        echo "'\$1' bukan file yang valid"
    fi
}

