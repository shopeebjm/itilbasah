precmd() {
    local EXIT_STATUS=\$?
    if [ -d "\$PWD" ]; then
        DIR_COLOR="magenta"
    else
        DIR_COLOR="red"
    fi
    if [[ \$EXIT_STATUS -eq 0 ]]; then
        PROMPT_SYMBOL="►"
        PROMPT_COLOR="green"
        setsid mpv --really-quiet --volume=100 "$PREFIX/.klik" >/dev/null 2>&1 < /dev/null
    else
        PROMPT_SYMBOL="►"
        PROMPT_COLOR="red"
        setsid mpv --really-quiet --volume=100 "$PREFIX/.salah" >/dev/null 2>&1 < /dev/null
    fi
    local GIT_BRANCH=""
    if command git rev-parse --is-inside-work-tree &>/dev/null; then
        GIT_BRANCH="\$(git branch --show-current 2>/dev/null)"
        [ -n "\$GIT_BRANCH" ] && GIT_BRANCH=" %F{yellow}[\$GIT_BRANCH]%f"
    fi

    local emots=("☕" "🖥️" "🎭" "🤨" "🐉" "📡")
    local rand_index=\$(( RANDOM % \${#emots[@]} ))
    local rand_emot=\${emots[\$rand_index]}

PROMPT=\$'\n'"\
%F{blue}┌──⟮%f\$rand_emot%F{blue}⟯⟮%F{red} Root@%m%F{blue} ⟯⟮ %F{\$DIR_COLOR}%~%f\$GIT_BRANCH%F{blue} ⟯\
"\$'\n'"\
└──%F{\$PROMPT_COLOR}\$PROMPT_SYMBOL"
}


